# 21. Frontmatter delimiter correctness
# ---------------------------------------------------------------------------


class TestFrontmatterDelimiters:
    """Markdown files with frontmatter must have proper --- delimiters."""

    MARKDOWN_FILES = [
        "commands/verify.md",
        "agents/vvuq-verifier.md",
        "skills/verify-claim/SKILL.md",
    ]

    @pytest.mark.parametrize("relpath", MARKDOWN_FILES)
    def test_frontmatter_has_opening_and_closing_delimiters(self, relpath: str):
        text = _read_text(relpath)
        lines = text.split("\n")
        assert lines[0].strip() == "---", (
            f"{relpath} does not start with '---' frontmatter delimiter"
        )
        # Find closing delimiter (must be after line 0)
        closing_found = False
        for line in lines[1:]:
            if line.strip() == "---":
                closing_found = True
                break
        assert closing_found, (
            f"{relpath} is missing closing '---' frontmatter delimiter"
        )
