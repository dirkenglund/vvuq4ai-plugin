class TestSecurityScannerCoverage:
    """
    The security tests in test_security_violations.py define
    SCANNABLE_EXTENSIONS that determine which files are scanned for
    secrets, credentials, and internal infrastructure details. HTML
    files are NOT in that set, meaning any .html file would completely
    bypass security scanning.

    Architecture principle: Open/Closed -- the security scanner should
    be open to new file types without requiring modification to each
    individual test.
    """

    def test_all_text_files_are_scannable(self):
        """Every text file in the distribution should be covered by the
        security scanner's file type filter. Files that slip through
        could contain secrets, internal hostnames, or other sensitive
        data without detection."""
        # These are the extensions the security scanner covers
        security_scannable = {
            ".json", ".md", ".txt", ".yml", ".yaml", ".toml",
            ".cfg", ".ini", ".sh",
        }
        security_scannable_names = {".gitignore", "LICENSE"}

        skip_dirs = {".git", "__pycache__", ".pytest_cache", ".benchmarks", "tests"}
        uncovered = []
        for fpath in PLUGIN_ROOT.rglob("*"):
            if not fpath.is_file():
                continue
            if any(ex in fpath.parts for ex in skip_dirs):
                continue
            if fpath.name in security_scannable_names:
                continue
            if fpath.suffix.lower() in security_scannable:
                continue
            # This file would not be scanned by the security tests
            try:
                fpath.read_text(encoding="utf-8")
                # It is a text file that bypasses security scanning
                uncovered.append(
                    f"{fpath.relative_to(PLUGIN_ROOT)} (type: {fpath.suffix})"
                )
            except (UnicodeDecodeError, ValueError):
                # Binary file -- not relevant for text-based secret scanning
                continue

        assert uncovered == [], (
            f"Text files that bypass security scanning: {uncovered}. "
            f"The security scanner in test_security_violations.py only covers "
            f"these extensions: {sorted(security_scannable)}. "
            f"Any text file with a different extension can contain secrets, "
            f"internal hostnames, or credentials without being detected. "
            f"Either remove these files from the distribution or expand the "
            f"security scanner's SCANNABLE_EXTENSIONS."
        )


# ---------------------------------------------------------------------------
# 26. Plugin repo must not duplicate MCP server repo content
# ---------------------------------------------------------------------------


class TestNoMcpServerDuplication:
    """
    The VVUQ4AI system has two repos:
      - dirkenglund/vvuq4ai-plugin (this repo) -- client-side plugin
      - dirkenglund/vvuq-mcp (private) -- MCP server

    Files that implement server-side functionality (API endpoints,
    database schemas, web pages, server configs) must live exclusively
    in the server repo. Duplicating them here creates maintenance
    burden, version drift, and confusion about which copy is canonical.

    Architecture principle: Single source of truth.
    """

    # Patterns that indicate server-side content
    SERVER_CONTENT_INDICATORS = [
        # Web server configuration
        (re.compile(r"<html|<head|<body|<!DOCTYPE", re.IGNORECASE), "HTML document"),
        # Server-side templates (Jinja, EJS, etc.)
        (re.compile(r"\{\{.*\}\}|<%.*%>"), "Server template syntax"),
        # Nginx/Apache config
        (re.compile(r"server\s*\{|location\s*/|<VirtualHost"), "Web server config"),
        # Dockerfile / docker-compose
        (re.compile(r"^FROM\s+\w+|^services:\s*$", re.MULTILINE), "Docker config"),
        # systemd service files
        (re.compile(r"^\[Unit\]|^\[Service\]|^\[Install\]", re.MULTILINE), "systemd unit"),
    ]

    def test_no_server_content_in_distribution(self):
        """Scan all distribution files for patterns that indicate
        server-side content that belongs in the MCP server repo."""
        skip_dirs = {".git", "__pycache__", ".pytest_cache", ".benchmarks", "tests"}
        violations = []
        for fpath in PLUGIN_ROOT.rglob("*"):
            if not fpath.is_file():
                continue
            if any(ex in fpath.parts for ex in skip_dirs):
                continue
            try:
                content = fpath.read_text(encoding="utf-8")
            except (UnicodeDecodeError, ValueError):
                continue
            for pattern, description in self.SERVER_CONTENT_INDICATORS:
                if pattern.search(content):
                    violations.append(
                        f"{fpath.relative_to(PLUGIN_ROOT)}: "
                        f"contains {description}"
                    )
                    break  # One match per file is enough

        assert violations == [], (
            f"Server-side content found in plugin distribution:\n"
            + "\n".join(f"  {v}" for v in violations)
            + "\n\nThese files contain content that belongs in the MCP server "
            "repo (dirkenglund/vvuq-mcp), not the client-side plugin."
        )


