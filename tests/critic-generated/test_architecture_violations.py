def _parse_frontmatter(text: str) -> dict:
    """
    Parse YAML-style frontmatter delimited by '---' lines.
    Returns a dict of key-value pairs (simple string values only for
    top-level scalar fields; multi-line values are kept as strings).
    """
    lines = text.split("\n")
    if not lines or lines[0].strip() != "---":
        return {}
    end_idx = None
    for i, line in enumerate(lines[1:], start=1):
        if line.strip() == "---":
            end_idx = i
            break
    if end_idx is None:
        return {}

    # Minimal YAML-like parsing (handles name: value and name: >)
    fm: dict = {}
    current_key = None
    current_val_lines: list[str] = []

    for line in lines[1:end_idx]:
        # Check for a new key
        m = re.match(r"^(\w[\w-]*):\s*(.*)", line)
        if m:
            # Flush previous key
            if current_key is not None:
                fm[current_key] = "\n".join(current_val_lines).strip()
            current_key = m.group(1)
            val = m.group(2).strip()
            if val == ">" or val == "|":
                current_val_lines = []
            elif val.startswith("[") or val.startswith("{"):
                # Inline list/dict -- keep raw
                current_val_lines = [val]
            elif val == "":
                current_val_lines = []
            else:
                current_val_lines = [val]
        elif current_key is not None:
            # Continuation line
            current_val_lines.append(line.strip())

    # Flush last key
    if current_key is not None:
        fm[current_key] = "\n".join(current_val_lines).strip()

    return fm


