# 22. Server-side assets must not ship in the plugin distribution
# ---------------------------------------------------------------------------


class TestNoServerSideAssets:
    """
    A Claude Code plugin is a client-side artifact: JSON configs, markdown
    instructions, and a .mcp.json that points to a remote server. Files that
    belong to the MCP *server* (HTML pages, CSS, server configs, templates)
    violate separation of concerns and must not be distributed with the plugin.

    Architecture principle: Single Responsibility -- the plugin repo defines
    how the Claude client talks to the server; the server repo owns everything
    the server serves.

    Generated by: Architecture Critic Agent (Round 2)
    Date: 2026-03-03
    """

    # File types that are server-side assets, not plugin components.
    # Claude Code plugins consist of: JSON, markdown, and optionally
    # shell scripts (hooks). HTML, CSS, JS (frontend), and templates
    # are server-side concerns.
    SERVER_SIDE_EXTENSIONS = {".html", ".css", ".jsx", ".tsx", ".vue", ".svelte"}

    # Files that are legitimate even with the above extensions
    # (none expected, but structure allows exceptions)
    ALLOWED_EXCEPTIONS: set[str] = set()

    def test_no_html_files_in_plugin_root(self):
        """HTML files are server-side assets. A Claude Code plugin has no
        use for HTML -- it uses markdown for instructions and JSON for config.
        HTML files belong in the MCP server repo (dirkenglund/vvuq-mcp),
        not the plugin repo."""
        html_files = list(PLUGIN_ROOT.glob("*.html"))
        html_files = [f for f in html_files if f.name not in self.ALLOWED_EXCEPTIONS]
        assert len(html_files) == 0, (
            f"HTML file(s) found in plugin root: "
            f"{[f.name for f in html_files]}. "
            f"HTML pages are server-side assets and belong in the MCP server "
            f"repo (dirkenglund/vvuq-mcp), not the plugin distribution. "
            f"The plugin repo should only contain JSON configs, markdown "
            f"instructions, and optionally hook scripts."
        )

    def test_no_server_side_files_anywhere_in_tree(self):
        """Scan the entire plugin tree (excluding .git and tests) for
        server-side file types that should not be distributed."""
        skip_dirs = {".git", "__pycache__", ".pytest_cache", ".benchmarks", "tests"}
        violations = []
        for fpath in PLUGIN_ROOT.rglob("*"):
            if not fpath.is_file():
                continue
            if any(ex in fpath.parts for ex in skip_dirs):
                continue
            if fpath.name in self.ALLOWED_EXCEPTIONS:
                continue
            if fpath.suffix.lower() in self.SERVER_SIDE_EXTENSIONS:
                violations.append(str(fpath.relative_to(PLUGIN_ROOT)))

        assert violations == [], (
            f"Server-side asset(s) found in plugin distribution: {violations}. "
            f"These file types ({self.SERVER_SIDE_EXTENSIONS}) belong in the "
            f"MCP server repo, not the client-side plugin."
        )

    def test_privacy_html_not_in_plugin(self):
        """privacy.html is a web page served by the MCP server at
        https://vvuq.dirkenglund.org/privacy. It is a server-side
        concern and must not exist in the plugin repo.

        Correct location: dirkenglund/vvuq-mcp repo
        Plugin alternative: link to the hosted URL from README.md
        """
        privacy_path = PLUGIN_ROOT / "privacy.html"
        assert not privacy_path.exists(), (
            "privacy.html exists in the plugin repo. This is a server-side "
            "web page that should be hosted by the MCP server and served at "
            "https://vvuq.dirkenglund.org/privacy. Move it to the "
            "dirkenglund/vvuq-mcp repo and add a link to the hosted page "
            "in the plugin's README.md instead."
        )


