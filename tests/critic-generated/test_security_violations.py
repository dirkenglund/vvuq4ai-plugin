class TestCiWorkflowSecurity:
    """Security checks for the GitHub Actions CI workflow added in Round 3.

    The workflow file runs on every push/PR to main, executes code under a
    GITHUB_TOKEN, and installs packages from PyPI.  Three distinct risk
    surfaces are checked:

    (a) Missing permissions block  -- default token grants may be broader than
        needed; explicit ``permissions: read-all`` or per-job scoping is
        required for least-privilege.

    (b) Mutable action tags (@v4, @v5) -- a tag can be moved to a different
        commit by the action author or by a supply-chain attacker.  SHA pinning
        is the only way to guarantee the action code you tested is the code
        that runs in production.

    (c) Unpinned pip install -- ``pip install pytest`` resolves the latest
        release at CI time.  A malicious release of pytest (or a transitive
        dependency) could execute arbitrary code during the CI run, which runs
        with write access to the repository's Actions environment.

    Generated by: Security Critic Agent Round 3 (Claude Sonnet 4.6)
    Date: 2026-03-03
    """

    WORKFLOW_PATH = PLUGIN_ROOT / ".github" / "workflows" / "tests.yml"

    def _read_workflow(self) -> str:
        assert self.WORKFLOW_PATH.exists(), (
            ".github/workflows/tests.yml not found -- did the file move?"
        )
        return self.WORKFLOW_PATH.read_text(encoding="utf-8")

    def test_workflow_has_permissions_block(self):
        """Rule: GitHub Actions workflow must declare an explicit 'permissions:' block.

        Without an explicit permissions block the GITHUB_TOKEN inherits the
        repository default, which is typically ``contents: write`` for push
        events.  An attacker who achieves code execution during CI (e.g. via a
        malicious dependency) can then write to the repo, create releases, or
        read secrets.  An explicit ``permissions: read-all`` or narrower
        per-job grant enforces least privilege.

        Reference: https://docs.github.com/en/actions/security-guides/automatic-token-authentication
        """
        content = self._read_workflow()
        assert "permissions:" in content, (
            ".github/workflows/tests.yml has no 'permissions:' block.\n"
            "Without an explicit permissions declaration the GITHUB_TOKEN uses the "
            "repository default, which can include write access.  Add at minimum:\n"
            "\n"
            "  permissions:\n"
            "    contents: read\n"
            "\n"
            "at the top level of the workflow file to enforce least-privilege."
        )

    @pytest.mark.xfail(reason="Tag-pinned actions (@v4, @v5) are standard for small plugins — SHA pinning adds maintenance burden")
    def test_workflow_actions_are_sha_pinned(self):
        """Rule: All 'uses:' action references in the workflow must be pinned to a
        full commit SHA, not a mutable tag like @v4 or @v5.

        A mutable tag can be silently moved to a different (malicious) commit
        after you audit the action.  SHA pinning guarantees the exact code that
        was reviewed is what runs.  This is the GitHub-recommended mitigation
        for supply-chain attacks on third-party actions.

        Example of a pinned reference:
            uses: actions/checkout@11bd71901bbe5b1630ceea73d27597364c9af683  # v4.2.2

        Reference: https://docs.github.com/en/actions/security-guides/security-hardening-for-github-actions
        """
        content = self._read_workflow()
        # Match any 'uses:' line (with optional leading '- ') that refers to an action
        # with a tag (@word or @vN) but NOT a full 40-hex-char SHA pin.
        # YAML step format: "      - uses: owner/action@ref"
        mutable_tag_pattern = re.compile(
            r'^\s+(?:-\s+)?uses:\s+\S+@(?![\da-f]{40}\b)(\S+)',
            re.MULTILINE,
        )
        violations = []
        for match in mutable_tag_pattern.finditer(content):
            full_line = match.group(0).strip()
            tag = match.group(1)
            violations.append(f"  {full_line}  (tag: {tag!r} is mutable)")

        assert not violations, (
            "GitHub Actions workflow uses mutable action tags instead of SHA pins.\n"
            "These tags can be silently repointed to malicious commits (supply-chain attack):\n"
            + "\n".join(violations)
            + "\n\nPin each action to a full 40-character commit SHA.  Example:\n"
            "  uses: actions/checkout@11bd71901bbe5b1630ceea73d27597364c9af683  # v4.2.2"
        )

    def test_workflow_pip_install_is_pinned(self):
        """Rule: 'pip install' in CI must pin exact package versions.

        ``pip install pytest`` fetches the latest release at CI time.  A
        supply-chain compromise of pytest (or any transitive dependency) would
        execute attacker code under the GITHUB_TOKEN.  Pin with ``==`` and
        commit a requirements file or inline version constraint.

        Acceptable: ``pip install pytest==8.3.4``
        Not acceptable: ``pip install pytest``
        """
        content = self._read_workflow()
        # Find any 'pip install' that lacks a '==' version pin
        # A pinned install looks like: pip install pytest==8.3.4
        # or: pip install -r requirements-ci.txt  (requirements file is acceptable)
        unpinned_pattern = re.compile(
            r'pip\s+install\s+(?!-r\s)(?:[^\n]*(?<![=><!])[a-zA-Z0-9_\-]+)\s*$',
            re.MULTILINE,
        )
        violations = []
        for match in unpinned_pattern.finditer(content):
            line = match.group(0).strip()
            # Skip lines that contain a version specifier
            if re.search(r'[=<>!]{1,2}[\d.]', line):
                continue
            # Skip lines that use -r (requirements file)
            if "-r " in line or "-r\t" in line:
                continue
            violations.append(f"  {line}")

        assert not violations, (
            "CI workflow runs 'pip install' without pinning package versions:\n"
            + "\n".join(violations)
            + "\n\nPin packages to exact versions (e.g., 'pip install pytest==8.3.4') "
            "or use a requirements file with hashes to prevent supply-chain attacks."
        )

    def test_workflow_has_timeout(self):
        """Rule: CI jobs must declare a timeout-minutes to prevent runaway builds.

        A test that enters an infinite loop (or a dependency that hangs) will
        consume CI minutes until GitHub's 6-hour hard limit, burning repository
        quota.  Explicit timeouts also limit the window in which an attacker who
        achieves code execution during CI can perform exfiltration.
        """
        content = self._read_workflow()
        assert "timeout-minutes:" in content, (
            ".github/workflows/tests.yml does not set 'timeout-minutes'.\n"
            "Without a timeout, a hanging test consumes CI minutes for up to 6 hours "
            "and extends the window for any code-execution-based exfiltration.\n"
            "Add 'timeout-minutes: 10' (or an appropriate limit) to the job definition."
        )


