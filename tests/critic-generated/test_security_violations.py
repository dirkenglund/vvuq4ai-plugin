class TestReadmeBadgeSecurity:
    """Verify that badge URLs in README.md do not introduce security or
    privacy risks beyond what is already accepted for the privacy.html file.

    The Round 3 README change adds three shields.io badge image URLs.
    shields.io is a well-known, legitimate service, but external image loads
    from a public README have two distinct risks:

    (a) Tracking: shields.io (or any CDN serving badge images) receives the
        viewer's IP address and User-Agent on each README render.  This is
        a weaker form of the tracking-pixel concern already tested in
        TestPrivacyHtmlSecurity.  Unlike privacy.html (served to end users),
        the README is typically rendered by GitHub's servers, which proxy the
        image requests.  However, if the README is ever rendered locally or
        via other clients, direct tracking is possible.

    (b) Version badge staleness as an integrity signal: the hardcoded
        ``version-0.1.0`` in the shields.io static badge URL will silently
        display an incorrect version after any release.  A user relying on
        the badge to verify they are installing the expected version receives
        false assurance -- a deceptive integrity indicator.

    (c) Badge URLs must use HTTPS and must not embed credentials or
        redirect through an attacker-controlled domain.

    Generated by: Security Critic Agent Round 3 (Claude Sonnet 4.6)
    Date: 2026-03-03
    """

    README_PATH = PLUGIN_ROOT / "README.md"

    def _read_readme(self) -> str:
        assert self.README_PATH.exists(), "README.md not found"
        return self.README_PATH.read_text(encoding="utf-8")

    def _extract_badge_image_urls(self) -> list[tuple[int, str]]:
        """Return (line_number, url) pairs for all Markdown image URLs in README."""
        content = self._read_readme()
        results = []
        # Matches ![alt](url) and [![alt](img_url)](link_url)
        img_url_pattern = re.compile(r'!\[[^\]]*\]\(([^)]+)\)')
        for line_num, line in enumerate(content.splitlines(), 1):
            for m in img_url_pattern.finditer(line):
                results.append((line_num, m.group(1).strip()))
        return results

    def test_badge_urls_use_https(self):
        """Rule: All badge image URLs in README.md must use HTTPS.

        HTTP badge URLs are vulnerable to MITM replacement: a network attacker
        can swap the badge image for one that signals a false status (e.g.,
        'build: passing' when tests actually fail) or inject tracking content.
        """
        badge_urls = self._extract_badge_image_urls()
        if not badge_urls:
            pytest.skip("No image URLs found in README.md")
        violations = []
        for line_num, url in badge_urls:
            if url.startswith("http://"):
                violations.append(f"  README.md:{line_num}: {url}")
        assert not violations, (
            "README.md badge URLs use insecure HTTP (MITM / status-spoofing risk):\n"
            + "\n".join(violations)
            + "\nAll badge image URLs must use HTTPS."
        )

    def test_badge_urls_do_not_embed_credentials(self):
        """Rule: Badge image URLs must not contain embedded credentials."""
        badge_urls = self._extract_badge_image_urls()
        if not badge_urls:
            pytest.skip("No image URLs found in README.md")
        violations = []
        for line_num, url in badge_urls:
            if re.search(r'://[^/]*:[^/]*@', url):
                violations.append(f"  README.md:{line_num}: {url}")
        assert not violations, (
            "README.md badge URLs contain embedded credentials:\n"
            + "\n".join(violations)
        )

    def test_badge_urls_use_known_domains(self):
        """Rule: Badge image URLs must come from a known, trusted CDN.

        An attacker who controls the badge URL domain can serve an image that
        tracks readers, loads malicious content in some renderers, or spoofs
        build status.  Only explicitly-allowlisted domains should be used for
        badge images.
        """
        ALLOWED_BADGE_DOMAINS = {
            "img.shields.io",          # shields.io static and dynamic badges
            "shields.io",              # shields.io redirects
            "github.com",             # GitHub Actions badge endpoint
            "raw.githubusercontent.com",  # GitHub raw assets
            "badgen.net",              # alternative badge service
            "codecov.io",             # coverage badges
        }
        badge_urls = self._extract_badge_image_urls()
        if not badge_urls:
            pytest.skip("No image URLs found in README.md")
        violations = []
        for line_num, url in badge_urls:
            # Extract domain
            m = re.match(r'https?://([^/]+)', url)
            if not m:
                violations.append(
                    f"  README.md:{line_num}: cannot parse domain from URL: {url}"
                )
                continue
            domain = m.group(1).lower()
            if domain not in ALLOWED_BADGE_DOMAINS:
                violations.append(
                    f"  README.md:{line_num}: badge served from unknown domain '{domain}': {url}"
                )
        assert not violations, (
            "README.md loads badge images from domains not in the allowlist.\n"
            "Unknown badge domains can track readers or spoof status:\n"
            + "\n".join(violations)
            + f"\nAllowed domains: {ALLOWED_BADGE_DOMAINS}"
        )

    def test_version_badge_matches_plugin_json(self):
        """Rule: The hardcoded version string in the shields.io static version badge
        must match the version declared in plugin.json.

        A stale version badge tells users they are running version X when they
        may actually be running version Y.  This is a false integrity signal:
        a user checking the badge to confirm they have the right version will
        receive incorrect information after any release.

        This test will fail as a reminder to update the badge whenever the
        version is bumped in plugin.json.
        """
        content = self._read_readme()
        plugin_path = PLUGIN_ROOT / ".claude-plugin" / "plugin.json"
        plugin_data = json.loads(plugin_path.read_text(encoding="utf-8"))
        plugin_version = plugin_data.get("version", "")
        assert plugin_version, "plugin.json does not declare a 'version' field"

        # Find the shields.io static badge URL containing a version string
        # Pattern: img.shields.io/badge/version-X.Y.Z-color.svg
        version_badge_pattern = re.compile(
            r'img\.shields\.io/badge/[^"\')\s]*version[^"\')\s]*-([0-9]+\.[0-9]+\.[0-9]+)-',
            re.IGNORECASE,
        )
        match = version_badge_pattern.search(content)
        if not match:
            pytest.xfail(
                "No shields.io static version badge found in README.md -- "
                "cannot verify version consistency.  If a dynamic badge is used "
                "this check can be removed."
            )

        badge_version = match.group(1)
        assert badge_version == plugin_version, (
            f"README.md version badge shows '{badge_version}' but plugin.json "
            f"declares version '{plugin_version}'.\n"
            "The badge version is hardcoded and must be updated on every release. "
            "Users relying on the badge to verify their installed version will "
            "receive false assurance after a version bump."
        )
