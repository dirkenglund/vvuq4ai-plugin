class TestReadmeImplementationDisclosure:
    """Check that the README 'How It Works' section does not expose
    implementation details that help attackers craft evasion inputs.

    The README (added/updated in Round 3) now describes the internal check
    pipeline: library names, check count, and check categories.  Each piece
    of information reduces the search space for an adversary trying to craft
    a claim that passes verification despite being wrong.

    Generated by: Security Critic Agent Round 3 (Claude Sonnet 4.6)
    Date: 2026-03-03
    """

    README_PATH = PLUGIN_ROOT / "README.md"

    def _read_readme(self) -> str:
        assert self.README_PATH.exists(), "README.md not found in plugin root"
        return self.README_PATH.read_text(encoding="utf-8")

    @pytest.mark.xfail(reason="Public plugin README should explain how the plugin works — check count is user-facing documentation, not a security risk")
    def test_readme_does_not_enumerate_check_count(self):
        """Rule: README must not state the exact number of internal verification checks.

        Publishing the check count ("up to 3 checks") tells an attacker exactly
        how many distinct evasion strategies to prepare.  If they know there are
        exactly 3 checks, they can probe each boundary independently.  The count
        is an internal implementation detail that provides no user value and
        meaningful attacker value.
        """
        content = self._read_readme()
        # Look for explicit "up to N checks" or "N checks" patterns
        check_count_pattern = re.compile(
            r'\bup\s+to\s+\d+\s+checks?\b'
            r'|\bruns?\s+\d+\s+checks?\b'
            r'|\bperforms?\s+\d+\s+checks?\b',
            re.IGNORECASE,
        )
        match = check_count_pattern.search(content)
        assert not match, (
            f"README.md discloses the exact number of internal verification checks: "
            f"'{match.group(0)}'\n"
            "This tells attackers how many evasion strategies to prepare.  "
            "Replace with a user-facing description of outcomes, not internal counts.\n"
            f"Found near: {content[max(0,match.start()-40):match.end()+40]!r}"
        )

    @pytest.mark.xfail(reason="SymPy is open source and publicly known — naming it in docs is transparency, not a security risk")
    def test_readme_does_not_name_internal_libraries(self):
        """Rule: README must not name the specific libraries used to implement checks.

        Naming 'SymPy' in the How It Works section reveals which mathematical
        engine processes claims.  An attacker who knows the checker uses SymPy
        can test SymPy's known edge cases and limitations to craft claims that
        SymPy evaluates incorrectly.  Library names are implementation details;
        the README should describe capabilities, not implementations.
        """
        content = self._read_readme()
        # Libraries that are internal implementation details
        internal_libraries = [
            "SymPy",
            "sympy",
        ]
        violations = []
        for lib in internal_libraries:
            # Only flag if in a descriptive context (not a code block showing user commands)
            for line_num, line in enumerate(content.splitlines(), 1):
                if lib in line:
                    # Allow if it is inside a code fence (user-visible command)
                    # We check the broader context but flag the line
                    violations.append(
                        f"  README.md:{line_num}: names internal library '{lib}': "
                        f"{line.strip()[:120]}"
                    )
        assert not violations, (
            "README.md names internal verification libraries, exposing implementation "
            "details that help attackers find evasion paths:\n"
            + "\n".join(violations)
            + "\nReplace library names with capability descriptions "
            "(e.g., 'mathematical consistency checks' instead of 'SymPy')."
        )

    @pytest.mark.xfail(reason="vvuq_resolve is the public API — documenting it helps users, and prompt injection is mitigated by quoting")
    def test_readme_does_not_expose_internal_function_names(self):
        """Rule: README must not expose the internal MCP tool function name.

        The README currently shows 'Claude calls vvuq_resolve() on the VVUQ MCP
        service'.  While the function name is also present in the skill/agent
        instruction files (which is required for operation), its appearance in
        the public README — linked from the marketplace listing — broadcasts the
        exact function signature to anyone who reads the page.  A prompt-
        injection attacker who knows the function name can craft claims that
        attempt to call vvuq_resolve() with attacker-controlled arguments.

        The How It Works section should describe the flow in user-facing terms
        ('the plugin contacts the VVUQ service') without exposing the function
        call syntax.
        """
        content = self._read_readme()
        # Match vvuq_resolve( in a descriptive prose context outside code fences
        # We scan line by line; code blocks in README use ``` delimiters
        in_code_block = False
        violations = []
        for line_num, line in enumerate(content.splitlines(), 1):
            stripped = line.strip()
            if stripped.startswith("```"):
                in_code_block = not in_code_block
            if not in_code_block and re.search(r'vvuq_resolve\s*\(', line):
                violations.append(
                    f"  README.md:{line_num}: {line.strip()[:120]}"
                )
        assert not violations, (
            "README.md exposes the internal MCP function call syntax 'vvuq_resolve()' "
            "in prose (outside a code fence).\n"
            "This gives prompt-injection attackers the exact function name to target.\n"
            "Violations:\n" + "\n".join(violations) + "\n"
            "Replace with user-facing language such as 'contacts the VVUQ service'."
        )


